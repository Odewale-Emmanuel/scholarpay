// Public payment page mock data
export const getPublicFeeByToken = (token: string) => {
  const fee = mockFeesWithStudents.find((f) => f.paymentToken === token);
  if (!fee) return null;
  const installments = mockInstallments.filter((i) => i.feeRecordId === fee.id);
  return { fee, installments };
};
